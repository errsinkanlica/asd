    public void calisanGoruntule() {
        
        model.setRowCount(0);
        
        ArrayList<Calisan> calisanlar = new ArrayList<Calisan>();
        
        calisanlar = islemler.calisanlariGetir();
        
        if (calisanlar !=null) {
            
            for(Calisan calisan: calisanlar){
                Object[] eklenecek = {calisan.getId(), calisan.getAd(), calisan.getSoyad(), calisan.getDepartman(), calisan.getMaas(), calisan.getDogumTarihi(),calisan.getAdres()};
                
                model.addRow(eklenecek);
            }
        }
        
        
        
    }
