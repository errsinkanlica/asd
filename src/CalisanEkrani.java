    private void guncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guncelleActionPerformed
        // TODO add your handling code here:
        
        String ad = ad_alani.getText();
        String soyad = soyad_alani.getText();
        String departman = dept_alani.getText();
        String maas = maas_alani.getText();
        String dogumTarihi = dogum_alani.getText();
        String adres = adres_alani1.getText();
        
        int selectedrow = calisan_tablosu.getSelectedRow();
        
        if(selectedrow == -1) {
            if (model.getRowCount() == 0) {
                mesaj_yazisi.setText("Çalışanlar Tablosu boş");
            }
            else {
                mesaj_yazisi.setText("Lütfen Güncellenecek bir çalışan seçin.");
            }
        }
        else {
            
            
            int id = (int)model.getValueAt(selectedrow, 0);
            
            islemler.calisanGüncelle(id, ad, soyad, departman, maas, dogumTarihi,adres);
            
            calisanGoruntule();
            
            mesaj_yazisi.setText("Çalışan Güncellendi.");
        }
        
    }//GEN-LAST:event_guncelleActionPerformed

    private void silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silActionPerformed
        // TODO add your handling code here:
        
        mesaj_yazisi.setText("");
        
        int selectedrow = calisan_tablosu.getSelectedRow();
        
        if (selectedrow == -1) {
            if (model.getRowCount() == 0 ) {
                mesaj_yazisi.setText("Çalışan Tablosu Boş..");
            }
            else {
                mesaj_yazisi.setText("Lütfen silinecek bir çalışan seçin");
            }
        }
        else {
            int id = (int)model.getValueAt(selectedrow, 0);
            
            islemler.calisanSil(id);
            
            calisanGoruntule();
            
            mesaj_yazisi.setText("Çalışan Silindi");
            
        }
    }//GEN-LAST:event_silActionPerformed

